(function initRotatingSubtitle() {
  var el = document.getElementById("rotatingSubtitle");
  if (!el) return;
  var altKeys = ["heroAlt1", "heroAlt2", "heroAlt3"];
  var index = 0;

  function rotate() {
    index = (index + 1) % altKeys.length;
    var text = translations[currentLanguage] && translations[currentLanguage][altKeys[index]];
    if (text) {
      el.classList.remove("fade-in");
      el.classList.add("fade-out");
      setTimeout(function () {
        el.textContent = text;
        el.classList.remove("fade-out");
        el.classList.add("fade-in");
      }, 350);
    }
  }
  setInterval(rotate, 5000);
})();

