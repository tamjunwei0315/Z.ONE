// Gallery Carousel
// =========================
(function initCarousel() {
  var slides = document.querySelectorAll(".slide");
  var dots = document.querySelectorAll(".dot");
  var prevBtn = document.getElementById("prevBtn");
  var nextBtn = document.getElementById("nextBtn");
  if (!slides.length) return;

  var current = 0;
  var autoInterval;

  function show(index) {
    if (index >= slides.length) current = 0;
    else if (index < 0) current = slides.length - 1;
    else current = index;

    slides.forEach(function (s) { s.classList.remove("active"); });
    dots.forEach(function (d) { d.classList.remove("active"); });

    slides[current].classList.add("active");
    dots[current].classList.add("active");
  }

  function startAuto() {
    clearInterval(autoInterval);
    autoInterval = setInterval(function () { show(current + 1); }, 6000);
  }

  prevBtn?.addEventListener("click", function () { show(current - 1); startAuto(); });
  nextBtn?.addEventListener("click", function () { show(current + 1); startAuto(); });

  dots.forEach(function (dot) {
    dot.addEventListener("click", function () {
      show(Number(dot.getAttribute("data-slide")));
      startAuto();
    });
  });

  show(0);
  startAuto();
})();

