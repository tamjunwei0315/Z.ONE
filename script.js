let currentLanguage = localStorage.getItem("language") || "zh";

function applyLanguage(language) {
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var key = el.getAttribute("data-i18n");
    if (translations[language] && translations[language][key]) {
      el.textContent = translations[language][key];
    }
  });

  var sub = document.getElementById("rotatingSubtitle");
  if (sub && translations[language] && translations[language].heroAlt1) {
    sub.textContent = translations[language].heroAlt1;
  }

  var toggle = document.getElementById("languageToggle");
  if (toggle) toggle.textContent = language === "en" ? "\u4e2d\u6587" : "EN";

  document.documentElement.lang = language === "en" ? "en" : "zh";
  localStorage.setItem("language", language);
}

document.getElementById("languageToggle")?.addEventListener("click", function () {
  currentLanguage = currentLanguage === "en" ? "zh" : "en";
  applyLanguage(currentLanguage);
});

